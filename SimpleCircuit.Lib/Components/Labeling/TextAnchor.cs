namespace SimpleCircuit.Components.Labeling
{
    /// <summary>
    /// Possible anchor points.
    /// </summary>
    public enum TextAnchor
    {
        /// <summary>
        /// Center of the text.
        /// </summary>
        Center,

        /// <summary>
        /// Middle left of the text.
        /// </summary>
        MiddleLeft,

        /// <summary>
        /// Top left of the text.
        /// </summary>
        TopLeft,

        /// <summary>
        /// Top center of the text.
        /// </summary>
        TopCenter,

        /// <summary>
        /// Top right of the text.
        /// </summary>
        TopRight,

        /// <summary>
        /// Middle right of the text.
        /// </summary>
        MiddleRight,

        /// <summary>
        /// Bottom right of the text.
        /// </summary>
        BottomRight,

        /// <summary>
        /// Bottom center of the text.
        /// </summary>
        BottomCenter,

        /// <summary>
        /// Bottom left of the text.
        /// </summary>
        BottomLeft,

        /// <summary>
        /// The text anchor point origin.
        /// </summary>
        Origin,

        /// <summary>
        /// The center point on the baseline.
        /// </summary>
        BaselineCenter,

        /// <summary>
        /// The text end point on the baseline.
        /// </summary>
        BaselineEnd,

        /// <summary>
        /// The text anchor point origin but at the top.
        /// </summary>
        TopBegin,

        /// <summary>
        /// The text end but at the top.
        /// </summary>
        TopEnd,

        /// <summary>
        /// The text anchor point origin but at the bottom.
        /// </summary>
        BottomBegin,

        /// <summary>
        /// The text end but at the bottom.
        /// </summary>
        BottomEnd,
    }
}
