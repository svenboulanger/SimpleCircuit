namespace SimpleCircuit.Diagnostics
{
    /// <summary>
    /// Possible severity levels for diagnostic messages.
    /// </summary>
    public enum SeverityLevel
    {
        /// <summary>
        /// An informational message.
        /// </summary>
        Info,

        /// <summary>
        /// A warning message.
        /// </summary>
        Warning,

        /// <summary>
        /// An error message.
        /// </summary>
        Error
    }
}
